                <h2>Sejarah Masjid</h2>
                <p>
                    Masjid Al-Bayaan di Politeknik Negeri Lhokseumawe berdiri sebagai salah satu ikon penting bagi
                    kampus
                    dan masyarakat sekitarnya. Sejarah pendirian masjid ini berawal dari kebutuhan mendesak akan tempat
                    ibadah yang memadai untuk mahasiswa, staf, dan warga sekitar. Pada awal 1990-an, Politeknik Negeri
                    Lhokseumawe mulai berkembang pesat sebagai lembaga pendidikan tinggi di Aceh. Seiring dengan
                    pertumbuhan ini, jumlah mahasiswa dan staf yang semakin banyak memerlukan fasilitas yang mampu
                    mendukung kegiatan spiritual mereka. Pada tahun 1995, sekelompok dosen dan mahasiswa mengajukan
                    usulan untuk membangun masjid di dalam lingkungan kampus. Usulan ini mendapat dukungan luas dari
                    seluruh civitas akademika yang merasa bahwa keberadaan masjid sangat penting sebagai pusat ibadah
                    dan kegiatan keagamaan.
                </p>
                <p>Penggalangan dana untuk pembangunan masjid dilakukan dengan penuh semangat. Mahasiswa, dosen, serta
                    staf kampus mengadakan berbagai kegiatan amal dan mengumpulkan donasi dari berbagai pihak. Dukungan
                    juga datang dari masyarakat luas yang memiliki kepedulian terhadap pembangunan sarana ibadah di
                    kampus tersebut. Setelah melewati berbagai tahap perencanaan dan persiapan, pembangunan masjid
                    dimulai pada tahun 1997. Proses pembangunan ini melibatkan banyak pihak, termasuk arsitek,
                    kontraktor lokal, dan para mahasiswa yang secara sukarela membantu dengan tenaga mereka. Semangat
                    gotong royong ini menjadi salah satu kekuatan utama yang mendorong kelancaran pembangunan masjid.
                </p>


                <p> Pada tahun 1999, Masjid Al-Bayaan akhirnya diresmikan dengan penuh rasa syukur dan kegembiraan. Nama
                    "Al-Bayaan," yang berarti "keterangan" atau "penjelasan," dipilih dengan harapan bahwa masjid ini
                    akan menjadi pusat pembelajaran dan pencerahan spiritual bagi seluruh civitas akademika. Masjid
                    Al-Bayaan tidak hanya berfungsi sebagai tempat ibadah, tetapi juga menjadi pusat berbagai kegiatan
                    keagamaan dan sosial. Setiap hari, masjid ini dipenuhi oleh mahasiswa yang melaksanakan shalat,
                    mengikuti kajian, dan terlibat dalam berbagai kegiatan keislaman lainnya. Selain itu, Masjid
                    Al-Bayaan juga sering menjadi tuan rumah berbagai acara besar seperti peringatan hari-hari besar
                    Islam, seminar keagamaan, dan kegiatan sosial lainnya yang melibatkan masyarakat sekitar.</p>

                <p>Seiring berjalannya waktu, Masjid Al-Bayaan mengalami beberapa kali renovasi untuk memperbaiki dan
                    memperluas fasilitas yang ada. Renovasi ini dilakukan untuk menjaga kenyamanan dan keindahan masjid
                    serta menambah kapasitas agar bisa menampung lebih banyak jamaah. Peran masjid ini dalam
                    mengembangkan kehidupan spiritual dan sosial di lingkungan kampus dan sekitarnya sangatlah besar.
                    Masjid Al-Bayaan menjadi tempat yang menyatukan berbagai elemen masyarakat dalam kebersamaan dan
                    kekhusyukan beribadah. Keberadaan masjid ini juga memberikan dampak positif bagi perkembangan
                    karakter dan moral mahasiswa, menjadikannya sebagai pusat kegiatan yang mendukung pembentukan
                    generasi yang berakhlak mulia.</p>

                <p>
                    Dengan sejarah yang panjang dan peran yang signifikan, Masjid Al-Bayaan terus menjadi pusat
                    spiritual dan sosial yang penting bagi Politeknik Negeri Lhokseumawe dan masyarakat sekitarnya.
                    Melalui berbagai kegiatan dan program yang diadakan, masjid ini terus memberikan kontribusi positif
                    bagi pengembangan pendidikan, keagamaan, dan sosial di wilayah tersebut. Masjid Al-Bayaan bukan
                    hanya sekadar bangunan fisik, tetapi juga simbol kebersamaan, dedikasi, dan semangat gotong royong
                    seluruh civitas akademika dan masyarakat dalam membangun sarana ibadah yang bermanfaat bagi semua.
                </p>

                <h2>Perkembangan dan Renovasi</h2>
                <p>Sejak didirikan, Masjid Albayaan PNL telah mengalami beberapa kali renovasi untuk meningkatkan
                    kapasitas
                    dan kenyamanan jamaah. Renovasi mencakup perbaikan fasilitas wudhu, penambahan ruang shalat, dan
                    pembaruan interior masjid.</p>

                <h2>Kegiatan dan Program</h2>
                <ul>
                    <li>Pengajian rutin untuk mahasiswa dan staf</li>
                    <li>Kelas-kelas tafsir Al-Qur'an dan hadits</li>
                    <li>Kegiatan sosial dan kemanusiaan di dalam dan luar kampus</li>
                    <li>Perayaan hari besar Islam seperti Idul Fitri dan Idul Adha</li>
                    <li>Kegiatan pembinaan rohani dan dakwah</li>
                </ul>
                <h2>Kontribusi terhadap Masyarakat Kampus</h2>
                <p>Masjid Albayaan PNL tidak hanya berfungsi sebagai tempat ibadah, tetapi juga sebagai pusat kegiatan
                    keagamaan dan sosial bagi komunitas kampus. Masjid ini menjadi tempat bagi mahasiswa untuk
                    memperdalam
                    ilmu agama, membangun kebersamaan, dan mengembangkan kegiatan positif.</p>

